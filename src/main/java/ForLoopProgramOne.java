public class ForLoopProgramOne {

    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Printing 1-10 values" + i);
        }
        for(int j=10;j>=1;j--){
            System.out.println("print descending order of int values" +j);
        }
    }
}
